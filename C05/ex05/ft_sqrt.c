int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	while (nb > 0 && i <= 46340)
	{
		if (nb == i * i)
			return (i);
		i++;
		if ((i * i) > nb)
			break ;
	}
	return (0);
}
